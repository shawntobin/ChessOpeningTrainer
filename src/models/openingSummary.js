class OpeningSummary {
  constructor(volume, id, name, moves, category, image, nameFilter, movesFilter) {
    this.volume = volume;
    this.id = id;
    this.name = name;
    this.moves = moves;
    this.category = category;
    this.image = image;
    this.nameFilter = nameFilter;
    this.movesFilter = movesFilter;
  }
}

export default OpeningSummary;
